void TIM14_init(void){
    RCC -> APB1ENR |= RCC_APB1ENR_TIM14EN;

    TIM14 -> CR1 &= ~(TIM_CR1_CMS | TIM_CR1_DIR); //edge-aligned, upcounting

    TIM14 -> PSC = 48000 - 1; //peripheral clock = 1000 Hz
    TIM14 -> ARR = 500 - 1; //interrupt every 1 Hz

    TIM14 -> DIER |= TIM_DIER_UIE; //update interrupt enable
    NVIC -> ISER[0] |= 1 << TIM14_IRQn;
    TIM14 -> CR1 |= TIM_CR1_CEN; //turn on TIM2 clock
}

void TIM14_IRQHandler(void){
    TIM14->SR &= ~TIM_SR_UIF;
    if(ENDD0 == RED0){
    	ENDD0 = BLANK;
    }
    else{
    	ENDD0 = RED0;
    }
}

