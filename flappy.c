void nano_wait(int n) {
    asm(    "        mov r0,%0\n"
            "repeat: sub r0,#83\n"
            "        bgt repeat\n" : : "r"(n) : "r0", "cc");
}

void barriergenerator(){
	int lower;  //highest vertical component of lower barrier
	int upper;  //lowest of upper
	int nextcolumn[2][1];  //the next vertical column not currently being displayed
	int ITERWIDTH = BARWIDTH + HGAP;  //number of pixels wide one cycle is

	int loop;  //loops through iteration
	int barloop;  //creates barrier
	int loop2;
	int scorehelper = 0;

	POSITION = 0;

	for(loop = 0; loop < 2; loop++){
		for(loop2 = 0; loop2 < 64; loop2++){
			display[loop][loop2] = 0;
		}
	}
	SCORE = 0;
	while(MODE == 1){
		lower = ((TIM3 -> CNT) % (64 - VGAP- 2)) + 1;  //TIM3 -> CNT generates random sequence
		upper = lower + VGAP;
		//nano_wait(100000000);

		for(loop = 0; loop < ITERWIDTH; loop++){
			nano_wait(1000000);
			POSITION = loop;

			if(loop < HGAP){
				for(barloop = 0; barloop < 64; barloop++){
					nextcolumn[barloop / 32][0] &= ~(1 << barloop % 32);
				}
			}
			else if(loop == HGAP || loop == ITERWIDTH - 1){
				for(barloop = 0; barloop < lower - 3; barloop++){
					nextcolumn[barloop / 32][0] &= ~(1 << barloop % 32);
				}  //sets parameters for lower barrier in next column
				for(; barloop < lower; barloop++){
					nextcolumn[barloop / 32][0] |= 1 << barloop % 32;
				}  //sets parameters for lower barrier in next column
				for(barloop = 0; barloop < VGAP; barloop++){
					nextcolumn[(lower + barloop) / 32][0] &= ~(1 << ((lower + barloop) % 32));
				}  //sets parameters for gap in next column
				for(barloop = upper; barloop < upper + 3; barloop++){
					nextcolumn[barloop / 32][0] |= 1 << barloop % 32;
				}  //sets parameters for upper barrier in next column
				for(; barloop < 64; barloop++){
					nextcolumn[barloop / 32][0] &= ~(1 << barloop % 32);
				}  //sets parameters for upper barrier in next column
			}
			else{
				for(barloop = 0; barloop < lower; barloop++){
					nextcolumn[barloop / 32][0] |= 1 << barloop % 32;
				}  //sets parameters for lower barrier in next column
				for(barloop = 0; barloop < VGAP; barloop++){
					nextcolumn[(lower + barloop) / 32][0] &= ~(1 << ((lower + barloop) % 32));
				}  //sets parameters for gap in next column
				for(barloop = upper; barloop < 64; barloop++){
					nextcolumn[barloop / 32][0] |= 1 << barloop % 32;
				}  //sets parameters for upper barrier in next column
			}

			for(barloop = 0; barloop < 63; barloop++){
				for(loop2 = 0; loop2 < 2; loop2++){
					display[loop2][barloop] = display[loop2][barloop + 1];
				}
			}
			for(loop2 = 0; loop2 < 2; loop2++){
				display[loop2][63] = nextcolumn[loop2][0];
			}
			scorehelper++;
			if(scorehelper / (HORIZPIX - BACKBIRD - BIRDWIDTH)){
				SCORE = (scorehelper - (HORIZPIX - BACKBIRD - BIRDWIDTH)) / ITERWIDTH;
			}
			setdisplay();
		}
	}
}




void GPIO_init(){
	//Red (PA1, PA2), Blue (PA3,PA4), Green (PA5,PA6)
	RCC->AHBENR |= RCC_AHBENR_GPIOAEN;

	GPIOA->MODER &= ~0xFFFF;
	GPIOA->MODER |= 0x1554; //Output

	//ABCDE - PC0, PC1, PC2, PC3, PC4
	RCC->AHBENR |= RCC_AHBENR_GPIOCEN;

	GPIOC->MODER &= ~0x3FF;
	GPIOC->MODER |= 0x155;

	//OE (PB0), Latch (PB1), CLK (PB3)
	RCC->AHBENR |= RCC_AHBENR_GPIOBEN;

	GPIOB->MODER &= ~(0xF | 0x3<<(2*3));
	GPIOB->MODER |= 0x1<<(2*0); //PB0 Output
	GPIOB->MODER |= 0x1<<(2*1); //PB1 Output
    GPIOB->MODER |= 0x1<<(2*3); //PB3 Output

    GPIOB->ODR |= 0x1; //Turn off OE
}

