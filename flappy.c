void birdgenerator(void){
	int loop, loop2;
	for(loop = 0; loop < 63; loop++){
		for(loop2 = 0; loop2 < 2; loop2++){
			BIRD[loop2][loop] = 0;
		}
	}
	for(loop = BACKBIRD; loop < BACKBIRD + BIRDWIDTH; loop++){
		for(loop2 = BIRDY; loop2 < BIRDY + BIRDHEIGHT; loop2++){
			BIRD[loop2 / 32][loop] |= 1 << (loop2 % 32);
		}
	}
	setdisplay();
}

