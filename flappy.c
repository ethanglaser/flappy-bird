void latch(int row){
    GPIOB->ODR |= 0x1; //Turn off OE
    //nano_wait(1000);

    GPIOB -> BSRR |= 0x2; //Latch
    nano_wait(1000);
    GPIOB -> BSRR |= 0x2 << 16;
    nano_wait(1000);

    setRow(row);

    GPIOB->ODR &= ~0x1; //Turn on OE
    //nano_wait(1000);
}

void get_color(int color){
    GPIOA->ODR &= ~(0x7E);
    GPIOA->ODR |= color << 1;
    //if((color == BLANK0) || (color == BLANK1)){
    //    GPIOA->ODR &= ~(color << 1);
    //}
}

void setRow(int row){
    GPIOC->BSRR |= 0xFF<<16; //Reset 1-5 of BR
    GPIOC->BSRR |= row; //Row 1 & 32
}

//determines how to write an up to 3 digit number on the screen
void num(int number){
	int mask = 0x1F;
	int loop;
	for(loop = 0; loop < 11; loop++){
		NUM[0][loop] = 0;
	}
	if(number / 100){
		for(loop = 0; loop < 3; loop++){
			NUM[0][loop + 8] = mask & (num5x3s[(number / 100) % 10] >> (5 * (2 - loop)));
		}
	}
	if(number / 10){
		for(loop = 0; loop < 3; loop++){
			NUM[0][loop + 4] = mask & (num5x3s[(number / 10) % 10] >> (5 * (2 - loop)));
		}
	}
	if(number){
		for(loop = 0; loop < 3; loop++){
			NUM[0][loop] = mask & (num5x3s[number % 10] >> (5 *(2 - loop)));
		}
	}
}

//writes display based on number, barriers, and bird
void setdisplay(void){
	int clk = 0;
	int x, y, z;
	int index = 0;
	int BC0 = GREEN0;
	int BC1 = GREEN1;
	//int NC0 = RED0;
	int NC1 = WHIT1;
	//int FC0 = ORANGE0;
	//int FC1 = ORANGE1;
	for(x = 0; x < 32; x++){
		for(y = 0; y < 64; y++){
			colordisplay[x][y] = 0;
		}
	}
	num(SCORE);
	for(x = 0; x < 2; x++){
		for(y = 0; y < 64; y++){
			if((display[x][y] & BIRD[x][y]) || ((BIRDY > 63) && (POSITION >= HGAP))){
				MODE = 2;
			}
			//combined[x][y] = display[x][y] | BIRD[x][y];
		}
	}
	//for(y = 0; y < 11; y++){
		//combined[NUMROW / 32][63 - (y + NUMCOL)] |= NUM[0][y] << NUMROW % 32;
	//}


	//convert existing matrix to combined with row 0 corresponding to top row and 32nd vertical, column 0 on left
	for(y = 0; y < 64; y++){
		x = 0;
		for(z = 0; z < 32; z++){
			if(BIRD[1][y] & (1 << z)){
				colordisplay[z][y] |= flapper[index] & WHIT0;
				index++;
			}
			else if(display[1][y] & (1 << z)){
				colordisplay[z][y] |= BC0;
			}
		}
		x = 1;
		for(z = 0; z < 32; z++){
			if((NUM[0][y] & (1 << z)) && (y < 11)){
				if(z < 5){
					colordisplay[NUMROW + z][63 - (y - NUMCOL)] |= NC1;
				}
			}
			else if(BIRD[0][y] & (1 << z)){
				colordisplay[z][y] |= flapper[index] & WHIT1;
				index++;
			}
			else if(display[0][y] & (1 << z)){
				colordisplay[z][y] |= BC1;
			}
		}

	}

    int row;
    int col;
    int rowSize = 32;
    int colSize = 64;
    int color;
    for(row = 0; row < rowSize; row++){
        for(col = 0; col < colSize; col++){
            color = colordisplay[row][63 - col];
            color = ((0x2A & color) >> 1) | ((0x15 & color) << 1);
            get_color(color);
            GPIOB -> BSRR |= 0x8; //CLK ON
            //nano_wait(1000);
            GPIOB -> BSRR |= 0x8 << 16; //CLK OFF
            //nano_wait(1000);
            clk++;
            if (clk == 64){
                clk = 0;
                latch(row);
            }
        }
    }
}

void EXTI0_1_IRQHandler(void){
	EXTI -> PR |= EXTI_PR_PR0;
	if(MODE == 0){
		BIRDY = STARTHEIGHT;
		MODE = 1;
	}
	else if(MODE == 1){
		BIRDY += JUMPHEIGHT / 3;
		birdgenerator();
		BIRDY += JUMPHEIGHT / 3;
		birdgenerator();
		BIRDY += JUMPHEIGHT / 3;
		birdgenerator();
	}
	else{
		MODE = 0;
	}
}

void jump(void){
	RCC->AHBENR |= RCC_AHBENR_GPIOAEN;
	EXTI -> IMR |= EXTI_IMR_MR0;
	EXTI -> RTSR |= EXTI_RTSR_TR0;
	NVIC -> ISER[0] |= 1 << EXTI0_1_IRQn;
}

void TIM2_IRQHandler(void){
	TIM2 -> SR &= ~TIM_SR_UIF;
	if(MODE == 1){
		BIRDY --;
		if(BIRDY < 0){
			MODE = 2;
		}
		birdgenerator();
	}
}

